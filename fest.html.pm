Transcyberian: Думай Как Лес – это трёхдневный рейв, совмещающий в себе несколько форматов: образовательные воркшопы, фестиваль экспериментальной электронной музыки и экологический лагерь. Рейв пройдёт на территории действующего эколагеря, который мы арендуем на время мероприятия, что гарантирует нам свободу от внешних помех и рисков.

Лагерь расположен на берегу реки, где можно купаться, а ближе к четырём утра к нам в гости будет приходить туман, на котором лазерными прожекторами мы будем рисовать очертания новых, возможных миров. 

Вместе мы создадим Временную Автономную Зону, где отношения техники и природы переопределены, центры и акценты свободно смещаются, иерархии ставятся под вопрос, структуры власти внешней и внутренней выносятся за скобки. 

Мы хотим вернуться к изначальным смыслам рейва и карнавала – празднованию бытия-вместе, упразднению отношений власти людей над людьми и людей над природой. 

Во время фестиваля произойдёт несколько запланированных хэппенингов, остальное же время будет отдано вам для того чтобы вы могли, пользуясь нашей инфраструктурой, проводить свои воркшопы, эксперименты, ритуалы.

