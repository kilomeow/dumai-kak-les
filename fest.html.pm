ТЕЛО!

Ваше тело тоже будет радо. Во-первых, во время фестиваля будет действовать горячая веганская кухня от наших друзей, специализирующихся на кормлении голодных рейверов. Мы обеспечим вас обедами и ужинами за донейшен или скромный прайс. Волонтёров и участников мы кормим бесплатно. На территории лагеря есть колодец с питьевой водой, к которой у вас будет безлимитный доступ. А также несколько мест для костров. Также будет чайная, кофе от сапатистов и пиво от товарищеского пивного кооператива. И, наконец, купание в речке и банный день в настоящей бане.


КОДЕКС


На фестивале действует главный принцип: будьте восхитительны друг к другу! Это значит, мы не приемлем никакой формы угнетения человека, животного или природы: сексизм, расизм, спешисизм, гомо, би, лесби, трансфобия. Если вы не разделяете этих ценностей, мы просим вас воздержаться от посещения нашего рейва. Мы хотим чтобы он был безопасным, комфортным местом для всех живых существ. 



СПОКОЙСТВИЕ


На территории фестиваля будет обеспечена постоянная психологическая и медицинская помощь, а также безопасный чиллаут, где вы можете найти покой, прилечь, побыть в тишине. На лагере предусмотрены сменяющиеся вахты доверия, это группы волонтёров, с которыми вы можете поговорить о ваших состояниях, переживаниях, страхах и радостях. Вы тоже можете побывать в этой роли, если чувствуете в себе потребность заботиться о других. 


