ПРОГРАММА


Фестиваль включает в себя две части: музыкальную и образовательную. Мы надеемся на симбиоз этих двух составляющих. Пространство лагеря подразумевает возможность проводить несколько воркшопов или выступлений одновременно. Вы сможете предложить свои воркшопы даже по ходу дела, уже на месте проведения фестиваля. 

Лагерь снабжён электричеством, проектором, связью, сохраняя при этом “тот самый” необходимый градус дикости. 

Образовательный трек содержит три основных темы: коммуникации, природа и сознание.
 В рамках трека коммуникации мы пригласили физиков-лириков, инженеро_к, шифопанков, которые не только расскажут об инструментах для децентрализации интернетов и безопасных коммуникациях, но и создадут реально функционирующую локальную беспроводную меш-сеть, которая будет покрывать весь лагерь. Они научат желающих разворачивать собственные подобные сети, чтобы общаться локально, не выходя в “большие интернеты”. Ожидайте также дискуссий о технотеологии, ярых споров техноскептиков и технооптимистов и игры “думай как лес” - о том как вдохновляться природным для дизайна технического.

В рамках трека природа мы пригласили экоактивисток, соларпанков, экофутуристо_в из нескольких низовых проектов, и мы надеемся обсудить вместе новую экологическую парадигму, возможность организации мероприятий по принципу zero waste, а также заняться праксисом – обустройством территории эколагеря в соответствии с нашими ценностями.

В рамках трека сознание мы проведём несколько групповых дискуссий о проблемах связанных со снижением рисков и осознанным подходом к изменению состояний сознания, а также поговорим о выгорании в активистской среде, тактиках бесконфликтной коммуникации. 


